//Digunakan untuk perulangan untuk data yang lebih bebas
//while adalah perulangan yang sangat fleksibel
//while akan terus melakukan perulangan jika kondisinya true, hingga kondisinya false while baru berhenti
//hati hati menggunakan while
fun main() {

    var i = 0

    while (i < 10){
        println("Perulangan $i")
        i++
    }
//    menggunakan keyword while
//    i++ artinya naik satu satu
//    tanda dollar untuk menggabungkan data

    println("Selesai perulangan")

}

