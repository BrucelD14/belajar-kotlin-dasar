//Do while loop hampir sama dengan perulangan while loop
//yang membedakan, pada Do while loop kode akan dijalankan dahulu, baru diakhir dilakukan pengecekan kondisi

fun main() {

    var i = 0

    do {
        println("Perulangan ke $i")
        i++
    } while (i < 10)
//menggunakan keyword do dan while
//    cara membedakannya dengan while loop adalah dengan membuat kondisinya false

    println("Selesai Perulangan")
}

