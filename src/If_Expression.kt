fun main() {

    val nilai = 50

    if (nilai > 90){
        println("Amazing")
    } else if (nilai > 75){
        println("Nice")
    } else if (nilai > 60){
        println("Not Bad")
    } else {
        println("Silahkan coba lagi, tetap semangat")
    }

//    diatas contoh else expression
//    digunakan apabila variable false
//    else if digunakan jika ada lebih dari 1 percobaan



}

